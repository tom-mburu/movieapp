package com.example.movieapp.models;

public class tvshowsModel {
    String tvshowBackdrop;
    String tvshowname;
    String tvshowOverview;
    float rating;
    int id;

    public String getTvshowBackdrop() {
        return tvshowBackdrop;
    }

    public void setTvshowBackdrop(String tvshowBackdrop) {
        this.tvshowBackdrop = tvshowBackdrop;
    }

    public String getTvshowname() {
        return tvshowname;
    }

    public void setTvshowname(String tvshowname) {
        this.tvshowname = tvshowname;
    }

    public String getTvshowOverview() {
        return tvshowOverview;
    }

    public void setTvshowOverview(String tvshowOverview) {
        this.tvshowOverview = tvshowOverview;
    }

    public float getRating() {
        return rating;
    }

    public void setRating(float rating) {
        this.rating = rating;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }
}
